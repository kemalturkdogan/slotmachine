    /* Create a new ImageIcon */
    public ImageIcon createImageIcon(String path, String description) {
        java.net.URL imgURL = getClass().getResource(path);
        if (imgURL != null) {
            return new ImageIcon(imgURL, description);
        } else {
            System.err.println("Couldn't find file: " + path);
            return null;
        }
    }

    public void matchControl() {
        if (reels1 == reels2 && reels2 == reels3) {

            jPanel2.setBackground(Color.red); // Highlights matched icons.
            jPanel3.setBackground(Color.red);
            jPanel4.setBackground(Color.red);
            jTextField3.setText(" " + (credit -= bet)); 
            jTextField4.setText(" " + df.format((source += getPrize(prize)))); // If there is a win add amount to cash pot.
            jTextField1.setText(" " + win()); // increment win amount.
        } else if (reels1 == reels2 || reels1 == reels3) {
            jTextField3.setText(" " + (credit -= bet));
            jTextField4.setText(" " + df.format((source += getPrize(prize))));
            jTextField1.setText(" " + win());

            if (reels1 == reels2) {
                jPanel2.setBackground(Color.red);
                jPanel3.setBackground(Color.red);
            } else if (reels1 == reels3) {
                jPanel2.setBackground(Color.red);
                jPanel4.setBackground(Color.red);
            }
        } else if (reels2 == reels3) {
            jPanel3.setBackground(Color.red);
            jPanel4.setBackground(Color.red);
            jTextField3.setText(" " + (credit -= bet));
            jTextField4.setText(" " + df.format((source += getPrize(prize))));
            jTextField1.setText(" " + win());
        } else {

            jTextField2.setText(" " + lose());
            jTextField3.setText(" " + (credit -= bet));
        }

    }

    public int lose() {
        lost++;
        return lost;
    }

    public int win() {
        win++;

        return win;
    }

