    /* Generates 3 random numbers */
    public void genRand() {

        Random rand = new Random();

        reels1 = rand.nextInt(images.size());
        reels2 = rand.nextInt(images.size());
        reels3 = rand.nextInt(images.size());

        setReels(reels1, reels2, reels3); // Set the reel image
    }

    /* Sets the reels in images ArrayList. */
    public void setReels(int ico1, int ico2, int ico3) {
        jLabel4.setIcon(images.get(ico1));
        jLabel5.setIcon(images.get(ico2));
        jLabel6.setIcon(images.get(ico3));
    }

    /*Player change money to credit */
    public void buyCredits() {
        if (source >= creditSell) {
            source -= creditSell;
            jTextField4.setText(" " + df.format(source));
            credit += creditPurchase;
            jTextField3.setText(" " + credit);

        }

    }

    /*  Calculate prizes */
    public double getPrize(double prize) {
        if (reels1 == reels2 && reels2 == reels3) {

            prize = this.prize; 
        } else if (reels1 == reels2 || reels1 == reels3 || reels2 == reels3) {

            prize = this.prize / 5; // if two are matched 
        } else {
            prize = 0; // If no win
        }
        return prize;
    }

    
